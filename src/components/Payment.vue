<template>
    <div>
        <h2>PAYMENT</h2>
        <p>To sign up for our service please click the checkout button below. Your $50 payment will automatically be completed monthly.</p>
        <!-- Create a button that your customers click to complete their purchase. Customize the styling to suit your branding. -->
        <div class="buttonDiv">
            <div>
                <button id="checkout-button-plan_GhtXUAapGzZiKS" role="link">
                    Checkout
                </button>
            </div> 
        </div>
        <div id="error-message"></div>
        <div class="fixed-action-btn">
            <router-link to="/landing" class="btn-floating btn-large grey">
                <i class="fa fa-arrow-left"></i>
            </router-link>
        </div>
    </div>
</template>
<script>
import firebase from 'firebase'
import db from './firebaseInit'
export default {
    name: 'payment',
    data() {
        return {
            users: [],
            email: '',
            user: firebase.auth().currentUser,
            user_id: null,
            paid: false
        }
    },
    methods: {
        saveUser () {
            db.collection('users')
                .add({
                email: this.email,
                user_id: this.user.uid,
                paid: true
            })
            //.then(docRef => this.$router.push('/'))
            .catch(error => console.log(err))
        }
    },
    created () {
        db
            .collection('users').where('user_id', '==', this.user_id)
            .get()
            .then(querySnapshot => {
                querySnapshot.forEach(doc => {
                const data = {
                    id: doc.id,
                    user_id: doc.data().user_id,
                    email: doc.data().mare_id,
                }
                this.users.push(data)
                })
            }) 
        if (this.user != null) {
            this.email = this.user.email
            this.user_id = this.user.uid
            saveUser()
        }

    },
    mounted () {
    (function() {
        var stripe = Stripe('pk_test_rmvxS3TrkGyvqF5nIitvGu8L00PK5JMuRs');

        var checkoutButton = document.getElementById('checkout-button-plan_GhtXUAapGzZiKS');
        checkoutButton.addEventListener('click', function () {
        // When the customer clicks on the button, redirect
        // them to Checkout.
        stripe.redirectToCheckout({
            items: [{plan: 'plan_GhtXUAapGzZiKS', quantity: 1}],

        // Do not rely on the redirect to the successUrl for fulfilling
        // purchases, customers may not always reach the success_url after
        // a successful payment.
        // Instead use one of the strategies described in
        // https://stripe.com/docs/payments/checkout/fulfillment
        successUrl: window.location.protocol + '//localhost:8080/#/',
        cancelUrl: window.location.protocol + '//localhost:8080/#/landing',
    })
    .then(function (result) {
      if (result.error) {
        // If `redirectToCheckout` fails due to a browser or network
        // error, display the localized error message to your customer.
        var displayError = document.getElementById('error-message');
        displayError.textContent = result.error.message;
      }
    })
  })
})()
    }
}
</script>
